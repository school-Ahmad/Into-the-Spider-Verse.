let totalWidth = 0;
let currentIndex = 0;

const generateCharacter = (character, index) => {
  const char = document.createElement("div");
  const img = new Image();
  img.src = character.img;
  img.width = "100%";
  img.height = "100%";
  img.style.position = "absolute";
  img.style.bottom = 0;
  char.appendChild(img);
  char.classList.add("character");

  // char.style.background = `url(${character.img}) no-repeat`;
  // char.style.backgroundSize = "contain";
  char.style.width = `300px`;
  char.style.height = `600px`;
  char.style.position = "relative";
  char.style.bottom = 0;

  // Initially set opacity to 0 for the fade-in effect
  char.style.opacity = 0;
  char.style.transition = "opacity 1s ease-in"; // Duration and easing of the fade-in effect

  // Add the character to the DOM
  document.body.appendChild(char); // Adjust the parent element as necessary

  // Trigger the fade-in effect on the next frame
  requestAnimationFrame(() => {
    char.style.opacity = 1;
  });

  return char;
};

const generateCharacterCard = (character) => {
  const card = document.getElementById("characterCard");
  card.innerHTML = `
    <div class="character-name">${character.name}</div>
    <div class="character-role">${character.role}</div>
    <div class="character-description">${character.description}</div>
    ${Object.entries(character.attributes)
      .map(
        ([key, value]) => `
      <div class="attribute">
        <span>${key.toUpperCase()}</span>
        <div class="bar">
          <div class="bar-inner" style="width: ${value}%;"></div>
        </div>
        <span>${value}</span>
      </div>
    `
      )
      .join("")}
    <div class="skills">
      ${character.skills
        .map(
          (skill) =>
            `<div class="skill-icon" style="background-image: url('${skill}');"></div>`
        )
        .join("")}
    </div>
  `;
};

const showGallery = () => {
  const galleryContainer = document.getElementById("galleryContainer");
  galleryContainer.innerHTML = "";
  totalWidth = 0;
  // characters.forEach((c, i) => {
  //   galleryContainer.appendChild(
  //     generateCharacter(c, i, characters.length - (i + 1))
  //   );
  // });
  galleryContainer.appendChild(
    generateCharacter(characters[currentIndex], currentIndex)
  );
  galleryContainer.style.width = `300px`;
  generateCharacterCard(characters[currentIndex]); // Display the first character's card initially
};

const nextCharacter = () => {
  // characters = [
  //   characters[characters.length - 1],
  //   ...characters.slice(0, characters.length - 1),
  // ];
  currentIndex = (currentIndex + 1) % characters.length;
  showGallery();
};

const prevCharacter = () => {
  // characters = [...characters.slice(1, characters.length), characters[0]];
  if (currentIndex - 1 < 0) currentIndex = characters.length - 1;
  else currentIndex--;
  showGallery();
};

showGallery();
